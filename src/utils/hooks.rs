    /// Helper to verify nested hook structure
    fn verify_hook_entry(entry: &serde_json::Value, expected_command: &str) -> bool {
        entry.get("matcher").map(|m| m == "").unwrap_or(false)
            && entry
                .get("hooks")
                .and_then(|h| h.as_array())
                .map(|arr| {
                    arr.iter().any(|hook| {
                        hook.get("type").map(|t| t == "command").unwrap_or(false)
                            && hook
                                .get("command")
                                .and_then(|c| c.as_str())
                                .map(|cmd| cmd == expected_command)
                                .unwrap_or(false)
                    })
                })
                .unwrap_or(false)
    }

