    #[test]
    fn test_validate_external_url_rejects_shell_metacharacters() {
        // Dollar sign (variable expansion)
        let result = validate_external_url("http://example.com/$HOME");
        assert!(result.is_err());
        assert!(result.unwrap_err().to_string().contains("forbidden characters"));

        // Backtick (command substitution)
        let result = validate_external_url("http://example.com/`whoami`");
        assert!(result.is_err());

        // Semicolon (command separator)
        let result = validate_external_url("http://example.com;rm -rf /");
        assert!(result.is_err());

        // Pipe (command piping)
        let result = validate_external_url("http://example.com|cat /etc/passwd");
        assert!(result.is_err());

        // Ampersand (background/command chaining)
        let result = validate_external_url("http://example.com&echo pwned");
        assert!(result.is_err());

        // Double quote
        let result = validate_external_url("http://example.com\"");
        assert!(result.is_err());

        // Single quote
        let result = validate_external_url("http://example.com'");
        assert!(result.is_err());

        // Backslash
        let result = validate_external_url("http://example.com\\n");
        assert!(result.is_err());

        // Newline
        let result = validate_external_url("http://example.com\necho pwned");
        assert!(result.is_err());

        // Carriage return
        let result = validate_external_url("http://example.com\recho pwned");
        assert!(result.is_err());
    }

